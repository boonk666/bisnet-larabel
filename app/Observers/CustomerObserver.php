    /**
     * Handle the Customer "created" event.
     *
     * @param  \App\Models\Customer  $customer
     * @return void
     */
    public function created(Customer $customer)
    {
        $user = User::create([
            'nama' => $customer->nama,
            'username' => $customer->nama,
            'password' => $customer->no_telp
        ]);

        $customer->update([
            'id_pengguna' => $user->id_pengguna
        ]);
    }

